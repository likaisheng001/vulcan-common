package com.vulcan.common.model;

public class DeptDetail {
private String dept_code;//部门编号
private String dept_id;//部门唯一编号
private String dept_name;//部门名称
private String dept_user;//部门负责人id
public String getDept_code() {
	return dept_code;
}
public void setDept_code(String dept_code) {
	this.dept_code = dept_code;
}
public String getDept_id() {
	return dept_id;
}
public void setDept_id(String dept_id) {
	this.dept_id = dept_id;
}
public String getDept_name() {
	return dept_name;
}
public void setDept_name(String dept_name) {
	this.dept_name = dept_name;
}
public String getDept_user() {
	return dept_user;
}
public void setDept_user(String dept_user) {
	this.dept_user = dept_user;
}


}
